    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            int bulan = jComboBox1.getSelectedIndex() + 1;
            int tahun = (int) jSpinner1.getValue();

            YearMonth yearMonth = YearMonth.of(tahun, bulan);
            int jumlahHari = yearMonth.lengthOfMonth();
            LocalDate hariPertama = yearMonth.atDay(1);
            LocalDate hariTerakhir = yearMonth.atEndOfMonth();
            boolean kabisat = Year.isLeap(tahun);

            String hasil = "Jumlah hari: " + jumlahHari + " hari\n" +
                        "Hari pertama: " + hariPertama.getDayOfWeek() + "\n" +
                        "Hari terakhir: " + hariTerakhir.getDayOfWeek() + "\n" +
                        "Tahun " + tahun + (kabisat ? " adalah tahun kabisat" : " bukan tahun kabisat");

            jTextArea1.setText(hasil);
        } catch (Exception e) {
            jTextArea1.setText("Terjadi kesalahan: " + e.getMessage());
        }    // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

