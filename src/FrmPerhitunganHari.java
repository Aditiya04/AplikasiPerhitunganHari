    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    java.util.Date date = jCalendar2.getDate();
    if (date == null) {
        jTextArea1.append("\nSilakan pilih tanggal terlebih dahulu di kalender!");
        return;
    }

    tanggalAwal = date.toInstant().atZone(java.time.ZoneId.systemDefault()).toLocalDate();
    jTextArea1.append("\nTanggal awal disimpan: " + tanggalAwal);
    jTextArea1.append("\nSekarang ubah tanggal di kalender untuk menentukan tanggal akhir lalu tekan hitung selisih.");    // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
    if (tanggalAwal == null) {
        jTextArea1.append("\nSilakan simpan tanggal awal terlebih dahulu!");
        return;
    }

    java.util.Date dateSekarang = jCalendar2.getDate();
    if (dateSekarang == null) {
        jTextArea1.append("\nSilakan pilih tanggal pada kalender!");
        return;
    }

    LocalDate tanggalAkhir = dateSekarang.toInstant()
            .atZone(java.time.ZoneId.systemDefault())
            .toLocalDate();

    long selisih = Math.abs(ChronoUnit.DAYS.between(tanggalAwal, tanggalAkhir));

    if (selisih == 0) {
        jTextArea1.append("\nTanggal yang dipilih sama. Silakan pilih tanggal akhir yang berbeda.");
    } else {
        jTextArea1.append("\nSelisih antara " + tanggalAwal + " dan " + tanggalAkhir + " adalah: " + selisih + " hari.");
    } // TODO add your handling code here:
    }//GEN-LAST:event_jButton4ActionPerformed

